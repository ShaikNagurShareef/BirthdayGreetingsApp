package com.project.bga.Model;

public class ExternalGreetings {

	private int friendId;
	private String friendName;
	private String friendPhoneNumber;
	private String friendEmail;
	private String friendMailSubject;
	private String friendMailMessage;
	private String friendRefereeName;
	private String friendRefereeEmail;

	public int getFriendId() {
		return friendId;
	}

	public void setFriendId(int friendId) {
		this.friendId = friendId;
	}

	public String getFriendName() {
		return friendName;
	}

	public void setFriendName(String friendName) {
		this.friendName = friendName;
	}

	public String getFriendPhoneNumber() {
		return friendPhoneNumber;
	}

	public void setFriendPhoneNumber(String friendPhoneNumber) {
		this.friendPhoneNumber = friendPhoneNumber;
	}

	public String getFriendEmail() {
		return friendEmail;
	}

	public void setFriendEmail(String friendEmail) {
		this.friendEmail = friendEmail;
	}

	public String getFriendMailSubject() {
		return friendMailSubject;
	}

	public void setFriendMailSubject(String friendMailSubject) {
		this.friendMailSubject = friendMailSubject;
	}

	public String getFriendMailMessage() {
		return friendMailMessage;
	}

	public void setFriendMailMessage(String friendMailMessage) {
		this.friendMailMessage = friendMailMessage;
	}

	public String getFriendRefereeName() {
		return friendRefereeName;
	}

	public void setFriendRefereeName(String friendRefereeName) {
		this.friendRefereeName = friendRefereeName;
	}

	public String getFriendRefereeEmail() {
		return friendRefereeEmail;
	}

	public void setFriendRefereeEmail(String friendRefereeEmail) {
		this.friendRefereeEmail = friendRefereeEmail;
	}

}
